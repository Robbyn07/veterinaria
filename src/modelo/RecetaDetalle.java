public class RecetaDetalle {
    
    private int recetaDetalleId;
    private String recetaDetalleNombre;
    private String recetaDetalleCantidad;
    private String recetaDetalleDosificacion;

    public int getRecetaDetalleId() {
        return recetaDetalleId;
    }

    public void setRecetaDetalleId(int recetaDetalleId) {
        this.recetaDetalleId = recetaDetalleId;
    }

    public String getRecetaDetalleNombre() {
        return recetaDetalleNombre;
    }

    public void setRecetaDetalleNombre(String recetaDetalleNombre) {
        this.recetaDetalleNombre = recetaDetalleNombre;
    }

    public String getRecetaDetalleCantidad() {
        return recetaDetalleCantidad;
    }

    public void setRecetaDetalleCantidad(String recetaDetalleCantidad) {
        this.recetaDetalleCantidad = recetaDetalleCantidad;
    }

    public String getRecetaDetalleDosificacion() {
        return recetaDetalleDosificacion;
    }

    public void setRecetaDetalleDosificacion(String recetaDetalleDosificacion) {
        this.recetaDetalleDosificacion = recetaDetalleDosificacion;
    }
    
    
    
    
}
